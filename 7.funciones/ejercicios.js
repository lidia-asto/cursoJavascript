function calcular (num1,num2,operacion){
    if(operacion=="suma"){
        total=num1+num2
    }
    if(operacion=="resta"){
        total=num1-num2
    }
    if(operacion=="multiplicacion"){
        total=num1*num2
    }
    if(operacion=="divicion"){
        total=num1/num2
    }
    return total
}
